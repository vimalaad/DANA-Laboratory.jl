export heat_rate
typealias Danaheat_rate DanaRealParametric
type _heat_rate
	function _heat_rate(_::Dict{Symbol,Any})
		fields::Dict{Symbol,Any}=(Symbol=>Any)[]
		fields[:Brief]="Heat Rate"
		fields[:Default]=1000
		fields[:Lower]=-1e9
		fields[:Upper]=1e9
		fields[:finalUnit]="kW"
		drive!(fields,_)
		new(fields)
	end
	value::Dict{Symbol,Any}
end
typealias heat_rate DanaRealParametric{_heat_rate}